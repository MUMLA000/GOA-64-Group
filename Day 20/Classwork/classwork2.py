# დაბეჭდეთ ეს და კიდევ შეამოწმეთ ლუწია თუ კენტი,
#  ხოლო თუ არაა დადებითი მხოლოდ დაბეჭდეთ რომ უარყოფითია

num = int(input("შეიყვანეთ რიცხვი: "))

if num > 0:
    print("დადებითი რიცხვია")
    if num % 2 == 0:
        print("ლუწია")
    else:
        print("კენტია")
else:
    print("უარყოფითი რიცხვია")