# 1. ცვლადის შექმნა
mybalance = 0  # დავიწყოთ ცვლადი mybalance 0-ით

# 2. მნიშვნელობის გაზრდა 2-ით (უბრალო ფორმით)
mybalance = mybalance + 2

# 3. მნიშვნელობის გაზრდა 3-ით assignment operator-ის გამოყენებით
mybalance += 3

# 4. მნიშვნელობის შეცვლა სურვილისამებრ assignment operator-ის გამოყენებით
mybalance *= 2

# 5. დაბეჭდვა
print(mybalance)

