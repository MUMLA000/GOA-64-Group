# მათემატიკური ოპერატორების მაგალითები

# დამატება (+)
print(5 + 3)
print(10 + 20)
print(7 + 2)
print(15 + 5)
print(100 + 200)

# გამოკლება (-)
print(10 - 3)
print(50 - 25)
print(100 - 55)
print(75 - 20)
print(200 - 100)

# გამრავლება (*)
print(4 * 3)
print(7 * 6)
print(10 * 2)
print(9 * 5)
print(12 * 11)

# გაყოფა (/)
print(10 / 2)
print(50 / 5)
print(100 / 10)
print(75 / 3)
print(200 / 25)

# ნაშთიანი გაყოფა (%)
print(10 % 3)
print(50 % 7)
print(100 % 6)
print(75 % 4)
print(200 % 9)

# ხარისხში აყვანა (**)
print(2 ** 3)
print(5 ** 2)
print(7 ** 3)
print(10 ** 4)
print(3 ** 5)

# მთელ რიცხვზე გაყოფა (//)
print(10 // 3)
print(50 // 7)
print(100 // 6)
print(75 // 4)
print(200 // 9)