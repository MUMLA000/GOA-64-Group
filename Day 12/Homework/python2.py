# ოპერატორი (operator) არის სიმბოლო, რომელიც ასრულებს მოქმედებას (მაგ: >, <, ==, !=).
# ოპერაცია (operation) არის ის კონკრეტული მოქმედება, რაც ოპერატორით ხდება (მაგ: 5 > 3).

# 5 ცვლადი სხვადასხვა შედარების ოპერაციის შედეგებით:
result1 = 10 > 5    # True - 10 მეტია 5-ზე
result2 = 7 <= 3    # False - 7 არ არის ნაკლები ან ტოლი 3-ის
result3 = -2 == -2  # True - -2 უდრის -2-ს
result4 = 0 != 1    # True - 0 არ არის ტოლი 1-ის
result5 = 8 >= 8    # True - 8 მეტია ან ტოლია 8-ის

# შედეგების დაბეჭდვა
print(result1)  # True
print(result2)  # False
print(result3)  # True
print(result4)  # True
print(result5)  # True