# > (მეტია)
print(10 > 5)   # True
print(3 > 8)    # False
print(-2 > -5)  # True
print(7 > 7)    # False
print(0 > -1)   # True

# >= (მეტია ან ტოლია)
print(10 >= 10)  # True
print(6 >= 3)    # True
print(2 >= 8)    # False
print(-5 >= -5)  # True
print(0 >= 1)    # False

# < (ნაკლებია)
print(4 < 9)   # True
print(7 < 2)   # False
print(-3 < -1) # True
print(5 < 5)   # False
print(0 < 1)   # True

# <= (ნაკლებია ან ტოლია)
print(4 <= 4)   # True
print(1 <= 5)   # True
print(9 <= 3)   # False
print(-2 <= -2) # True
print(0 <= -1)  # False

# == (ტოლია)
print(5 == 5)   # True
print(3 == 7)   # False
print(-4 == -4) # True
print(0 == 1)   # False
print("a" == "a") # True

# != (არ არის ტოლი)
print(5 != 3)   # True
print(2 != 2)   # False
print(-1 != 1)  # True
print(0 != 0)   # False
print("hello" != "world") # True