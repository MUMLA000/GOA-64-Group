# მომხმარებლის ასაკი
age = input("შეიყვანეთ თქვენი ასაკი: ")

# მომხმარებლის მიერ შემოტანილი წლების რაოდენობა
years = input("შეიყვანეთ რამდენი წლის შემდეგ გინდათ რომ გამოითვალოთ ასაკი: ")

# მონაცემთა ტიპების შეცვლა
age = int(age)
years = int(years)

# მომავალი ასაკის გამოთვლა
future_age = age + years

# შედეგის გამოტანა
print(str(years) + " წლის შემდეგ თქვენ იქნებით " + str(future_age) + " წლის.")
